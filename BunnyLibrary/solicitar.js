// Função para exibir a caixa de login
function mostrarCaixaLogin() {
    var caixaLogin = document.getElementById("caixa-login");
    caixaLogin.style.display = "block";
}

// Função para fechar a caixa de login
function fecharCaixaLogin() {
    var caixaLogin = document.getElementById("caixa-login");
    caixaLogin.style.display = "none";
}

